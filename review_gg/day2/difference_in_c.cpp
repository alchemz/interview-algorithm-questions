//difference between C and C++
/*
* 1. In C++, it is a compiler error to call a function before it is declared, but in C, it's allowed
* 2. In C++, it's compiler error to make a normal pointer to point to a constant. but allowed in C
* 3. In C, a void pointer can directly be assigned to some other pointer like int*, char*, 
	but in C++, a void pointer must be explicitly typcasted. 
* 4. In C++, const variable must be initialized, but in C, it isn't necessary.
* 5. C++ does more strict type checking than C
*/